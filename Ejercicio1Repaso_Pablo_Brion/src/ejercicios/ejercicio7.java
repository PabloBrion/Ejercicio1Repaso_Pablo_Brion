package ejercicios;
// Área de un triángulo
public class ejercicio7 {
    public static void main(String[] args) {
        // Declaración de las variables
        double base = 5;  // Variable 'base' con valor 5
        double altura = 8; // Variable 'altura' con valor 8

        // Cálculo del área usando la fórmula: área = (base * altura) / 2
        double area = (base * altura) / 2;

        // Impresión del resultado
        System.out.println("El área del triángulo con base " + base + " y altura " + altura + " es: " + area);
    }
}
