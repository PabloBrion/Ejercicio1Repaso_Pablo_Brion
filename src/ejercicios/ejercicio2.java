package ejercicios;
// Producto de dos números
public class ejercicio2 {

        public static void main(String[] args) {
            // Declaración de las variables
            int a = 7; // Variable 'a' con valor 7
            int b = 8; // Variable 'b' con valor 8

            // Cálculo del producto
            int producto = a * b; // Se multiplican 'a' y 'b' y el resultado se almacena en 'producto'

            // Impresión del resultado
            System.out.println("El producto de " + a + " y " + b + " es: " + producto); // Se muestra el resultado
        }
    }

