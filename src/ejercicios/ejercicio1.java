package ejercicios;
// Suma de dos números
public class ejercicio1 {

        public static void main(String[] args) {
            // Declaración de las variables
            int a = 10; // Variable 'a' con valor 10
            int b = 20; // Variable 'b' con valor 20

            // Cálculo de la suma
            int suma = a + b; // Se suman 'a' y 'b' y el resultado se almacena en 'suma'

            // Impresión del resultado
            System.out.println("La suma de " + a + " y " + b + " es: " + suma); // Se muestra el resultado
        }
    }

